package com.lzcb.bigitem.Entity;

public class Xwlm {
		
	
		private String id;
		private String lmmc;
		private String lmfl;
		private String myorder;
		public String getId() {
			return id;
		}
		public void setId(String id) {
			this.id = id;
		}
		public String getLmmc() {
			return lmmc;
		}
		public void setLmmc(String lmmc) {
			this.lmmc = lmmc;
		}
		public String getLmfl() {
			return lmfl;
		}
		public void setLmfl(String lmfl) {
			this.lmfl = lmfl;
		}
		public String getMyorder() {
			return myorder;
		}
		public void setMyorder(String myorder) {
			this.myorder = myorder;
		}
		@Override
		public String toString() {
			return "Xwlm [id=" + id + ", lmmc=" + lmmc + ", lmfl=" + lmfl
					+ ", myorder=" + myorder + "]";
		}
		
		
		
		
}
